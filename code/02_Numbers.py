# Ab hier stehen Beispiele für Floats
print(1.00000)
print(-1.0)
print(5.0)
print(-5.0)
print(10.999997)
print(-10.999997)

print("Addition")
print(3 + 4)  # 7
print(1.0 + 7.5)  # 8.5

print("Substraktion")
print(3 - 4)  # -1
print(1.0 - 7.5)  # -6.5

print("Multiplikation")
print(3 * 4)  # 12
print(3.0 * 7.0)  # 21.0

print("Division")
print(12 / 4)  # 3.0
print(12 / 5)  # 2.4
print(12.0 / 4.0)  # 3.0

print("Division (abrunden)")
print(12 // 5)  # 2.4 → 2
print(12.0 // 5.0)  # 2.0

print("Division mit Rest")
print(12 % 4)  # 12 / 4 → 3 Rest 0
print(12 % 5)  # 12 / 5 → 2 Rest 2
print(12 % 6)  # 12 / 6 → 2 Rest 0
print(12 % 7)  # 12 / 7 → 1 Rest 5

print(10.5 % 1.0)  # 0.5
print(10.5 % 0.5)  # 0.0
print(10.5 % 6.0)  # 4.5

print("Datentypen mischen")
print(1 + 2.0)  # 3.0
print(3.0 + 4)  # 7.0
print(12 % 6.0)  # 0.0
print(12.0 % 7)  # 5.0

